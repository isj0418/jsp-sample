package com.example.guest;

public class GuestVO {
    private String guestId;
    private int guestNumber;
    private String guestName;
    private String guestTitle;
    private String guestContent;
    private String guestWdate;
    private int guestHit;
    private String guestImgFile;

    public String getGuestId() {
        return guestId;
    }

    public void setGuestId(String guestId) {
        this.guestId = guestId;
    }

    public int getGuestNumber() {
        return guestNumber;
    }

    public void setGuestNumber(int guestNumber) {
        this.guestNumber = guestNumber;
    }

    public String getGuestName() {
        return guestName;
    }

    public void setGuestName(String guestName) {
        this.guestName = guestName;
    }

    public String getGuestTitle() {
        return guestTitle;
    }

    public void setGuestTitle(String guestTitle) {
        this.guestTitle = guestTitle;
    }

    public String getGuestContent() {
        return guestContent;
    }

    public void setGuestContent(String guestContent) {
        this.guestContent = guestContent;
    }

    public String getGuestWdate() {
        return guestWdate;
    }

    public void setGuestWdate(String guestWdate) {
        this.guestWdate = guestWdate;
    }

    public int getGuestHit() {
        return guestHit;
    }

    public void setGuestHit(int guestHit) {
        this.guestHit = guestHit;
    }

    public String getGuestImgFile() {
        return guestImgFile;
    }

    public void setGuestImgFile(String guestImgFile) {
        this.guestImgFile = guestImgFile;
    }
}
